    public Material getMaterial() {  
    	return material;  
    }  
    
    public static boolean isEarthBendingBlock(Material material) {
    	for (BendingBlock block : BendingBlock.values()) {
    		if (block.getMaterial().equals(material)) {
    			return true;
    		}
    	}
    	return false;
    }
